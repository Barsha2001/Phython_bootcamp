a = float(input("Enter the value of a: "))
b = float(input("Enter the value of b: "))


floor_division_result = a // b
print(f"Floor Division of a and b: {floor_division_result}")

power_result = a ** b
print(f"a raised to the power b: {power_result}")

equal_values = a == b
print(f"Are a and b equal?: {equal_values}")
